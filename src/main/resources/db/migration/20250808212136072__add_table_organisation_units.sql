CREATE TABLE
  organisation_units (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    uuid UUID NOT NULL,
    name VARCHAR(100) NOT NULL,
    code VARCHAR(10),
    phone_number VARCHAR(12),
    email VARCHAR(50),
    address TEXT,
    background TEXT,
    letter_head_title TEXT,
    level_id BIGINT NOT NULL,
    parent_id BIGINT,
    created_at TIMESTAMP WITHOUT TIME ZONE,
    created_by VARCHAR(120),
    updated_at TIMESTAMP WITHOUT TIME ZONE,
    updated_by VARCHAR(120),
    deleted_at TIMESTAMP WITHOUT TIME ZONE,
    deleted_by VARCHAR(120),
    CONSTRAINT pk_organisation_units PRIMARY KEY (id),
    CONSTRAINT uc_organisation_units_uuid UNIQUE (uuid),
    CONSTRAINT uc_organisation_units_name UNIQUE (name),
    CONSTRAINT uc_organisation_units_code UNIQUE (code),
    CONSTRAINT FK_organisation_units_ON_levels FOREIGN KEY (level_id) REFERENCES organisation_unit_levels (id),
    CONSTRAINT FK_organisation_units_ON_parents FOREIGN KEY (parent_id) REFERENCES organisation_units (id)
  );
